<h3>ABOUT OLUBANKE DAVID </h3>
<br><br>
Olubanke David is the Head of School at EPS Educational Academy.
She is a firm believer that all children can succeed given the right
training and under the right conditions.
She is a graduate of the University of Lagos, Akoka and also an alumnus
of the prestigious Harvard Graduate of School of Education, USA. She
is passionate about lifelong learning as well as helping youth actualize
their potential.
Her personal and professional goals include rebranding of schools in her
community, educating learners to become the best version of themselves
as well as contributing her quota to the development of the educational
sector.
She holds various certifications from several institutions including the
following: -
 Diploma in Early Childhood Education: Modern Montessori
Institute, London (M.M.I). October 2016.
 An Introduction to Core Skills for Leaders (British Council) April,
2020.
 Business Essentials (Harvard University) May, 2020.
 Rhetoric’s: The Art of Persuasive Writing and Public Speaking
(Harvard University) June, 2020.
 Business Resilience (Cherie Blaire Foundation for Women) July,
2020.
 Leading Schools (Harvard University). July, 2020.
 Leading People (Harvard University) August, 2020.
 Exercising Leadership (Harvard University) August, 2020.
 Leading Change (Harvard University) September, 2020.
