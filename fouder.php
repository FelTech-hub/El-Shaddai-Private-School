             <h3>  ABOUT THE FOUNDER</h3>
Mrs. Julie Olabosipo Olatunji (June 6, 1939-Nov 4, 2017) was the
founder of El-Shaddai Private School, Oregun, Ikeja. Armed with a
directive from God and compelling vision to raise a model school that
would provide solutions to the educational problems in the state, she
founded the school on the 26th of September 1994. She was very
passionate about the work and spent many years of meritorious service
in the public sector before her retirement from public service and
consequent establishment of El-Shaddai Private School.
An educator per excellence, she sat on various boards including but not
limited to:
 Lagos State Technical Committee for the establishment of the
industrial manpower institute at Odomila, Epe.
 Lagos State Technical Committee for policy formation of the
Lagos State University.
 Lagos State Committee on Education Policy formation and
implementation.
 Representative of the Ministry of Education on the School
Management Board.
 Representative of the Ministry of Education at the UNESCO
meetings.
 Presentation of papers on the development of education in Lagos
State as secretary of Schools Management Committee.
 Committee on the implementation of the 6-3-3-4 Education policy
in Lagos State.
 Committee on Unified Examinations and Unified promotion
criteria for schools in Lagos state.
 Committee on the involvement of PTA’S and voluntary
organizations in the development of schools in Lagos state.
 Committee on the maximization of physical facilities in Lagos
State schools.
 Committee on the deficiencies in the State Education system viz-aviz student performance in External Examinations.
 Organizing Secretary and Treasurer of the state A.N.C.O.P.S.S
 Chairperson Somolu Zonal A.N.C.O.P.S.S (1988-1993)
 A.N.C.O.P.S.S representative at the Educational Board of Studies
at L.A.S.U.
 Chairperson of the State A.N.C.O.P.S.S Advisory Committee on
Discipline for 2years.
 Chairperson, A.N.C.O.P.S.S National Congress Discussion Group.
May her soul continue to rest with the Lord and her legacies will forever
live on in Jesus name, amen
